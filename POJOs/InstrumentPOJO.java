package com.cenfotec.stretto.pojo;

public class InstrumentPOJO{
	
	private int id;
	private String name;;

	public InstrumentPOJO(){
		super();
	}

	public int getId() {
		return id;
	}
	public void setId(int pId) {
		this.id = pId;
	}
	public String getName() {
		return name;
	}
	public void setName(String pName) {
		this.name = pName;
	}
	
}
